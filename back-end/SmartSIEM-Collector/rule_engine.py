class CorrelationEngine:
    """The orchestration runtime processing raw data logs against dynamic rulesets."""
    
    def __init__(self):
        self.rules: List[SiemRule] = []
        self.alert_callback_hook = None
        self.action_callback_hook = None
        self.processed_log_count = 0

    def register_rule(self, rule: SiemRule):
        self.rules.append(rule)
        logger.info(f"Rule matching component '{rule.name}' successfully mounted into engine.")

    def set_output_hooks(self, on_alert, on_action=None):
        self.alert_callback_hook = on_alert
        self.action_callback_hook = on_action

    def dispatch_log(self, raw_payload: str) -> int:
        """Parses, normalizes, and feeds logs through every registered rule signature."""
        try:
            parsed = json.loads(raw_payload)
            normalized_log = SiemLog(
                log_id=parsed.get("id", f"LOG-{int(time.time()*1000)}"),
                tenant=parsed.get("tenant", "default"),
                timestamp=parsed.get("timestamp", int(time.time() * 1000)),
                service=parsed.get("service", "GenericEngine"),
                event_type=parsed.get("event_type", "GENERIC"),
                payload=parsed.get("payload", {}),
                tags=set(parsed.get("tags", []))
            )
            
            self.processed_log_count += 1
            matches_found = 0
            
            for rule in self.rules:
                match_results = rule.evaluate(normalized_log)
                if match_results:
                    matches_found += 1
                    alert, mitigation_request = match_results
                    
                    if self.alert_callback_hook:
                        self.alert_callback_hook(alert)
                    if mitigation_request and self.action_callback_hook:
                        self.action_callback_hook(mitigation_request)
                        
            return matches_found
        except Exception as err:
            logger.error(f"Failed to ingest log stream event framework payload: {str(err)}")
            return 0