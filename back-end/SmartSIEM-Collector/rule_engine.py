class BruteForceAuthRule(SiemRule):
    """Tracks sequential authorization failures over a sliding time-window configuration."""
    
    def __init__(self, failure_threshold: int = 3, lookback_ms: int = 60000):
        super().__init__(
            name="ruleset::authentication::brute_force",
            description="Detects multiple authentication failures targeting localized user accounts.",
            service="AuthService"
        )
        self.threshold = failure_threshold
        self.lookback_ms = lookback_ms
        self.state_lock = threading.Lock()
        self.user_tracking: Dict[str, List[int]] = {}
        
        self.templates["en"] = "Brute force pattern detected for user $username within domain $domain. Total failures: $count"

    def evaluate(self, log: SiemLog) -> Optional[Tuple[SiemAlert, Optional[Dict[str, Any]]]]:
        if log.event_type != "AUTH_FAILURE":
            return None
            
        username = log.get_field("auth.username")
        domain = log.get_field("auth.domain", "LOCAL")
        if not username:
            return None
            
        tracking_key = f"{domain}|{username}"
        current_time = log.timestamp
        
        with self.state_lock:
            if tracking_key not in self.user_tracking:
                self.user_tracking[tracking_key] = []
                
            history = self.user_tracking[tracking_key]
            history.append(current_time)
            
            # Prune events older than our window limit
            self.user_tracking[tracking_key] = [t for t in history if current_time - t <= self.lookback_ms]
            current_count = len(self.user_tracking[tracking_key])
            
            if current_count >= self.threshold:
                desc_tpl = self.get_localized_description(log.tenant, self.templates["en"])
                resolved_desc = (desc_tpl
                                 .replace("$username", username)
                                 .replace("$domain", domain)
                                 .replace("$count", str(current_count)))
                
                alert = SiemAlert(
                    alert_id=f"ALT-{log.log_id}-{int(time.time())}",
                    title="Suspicious Authentication Activity - Brute Force",
                    description=resolved_desc,
                    severity=AlertSeverity.HIGH,
                    creation_time=int(time.time() * 1000),
                    rule_name=self.name,
                    triggering_log=log,
                    mitre_tactics=["TA0001"], # Initial Access
                    mitre_techniques=["T1110"], # Brute Force
                    aggr_key=tracking_key,
                    aggr_limit=current_time + self.lookback_ms
                )
                
                # Request firewall or account lockout mitigation action
                mitigation = {
                    "action": "ACCOUNT_LOCK",
                    "target_user": username,
                    "target_domain": domain,
                    "expiry_ms": 1800000
                }
                return alert, mitigation
                
        return None

class PrivilegeEscalationRule(SiemRule):
    """Stateless analysis rule catching anomalous command execution strings."""
    
    def __init__(self):
        super().__init__(
            name="ruleset::privilege::execution_anomaly",
            description="Identifies dangerous process parameters executed by non-administrative sessions.",
            service="OS_Audit"
        )
        self.monitored_commands = {"chmod +x", "sudo su", "mimikatz", "vssadmin delete shadows"}
        self.templates["en"] = "High risk process execution string observed: '$command' spawned by session user '$user'"

    def evaluate(self, log: SiemLog) -> Optional[Tuple[SiemAlert, Optional[Dict[str, Any]]]]:
        if log.event_type != "PROCESS_SPAWN":
            return None
            
        cmd_line = log.get_field("process.command_line", "").lower()
        user_session = log.get_field("process.user", "unknown")
        
        matched_cmd = next((cmd for cmd in self.monitored_commands if cmd in cmd_line), None)
        
        if matched_cmd:
            desc_tpl = self.get_localized_description(log.tenant, self.templates["en"])
            resolved_desc = desc_tpl.replace("$command", matched_cmd).replace("$user", user_session)
            
            alert = SiemAlert(
                alert_id=f"ALT-{log.log_id}",
                title="Privilege Escalation Vector Identified",
                description=resolved_desc,
                severity=AlertSeverity.CRITICAL,
                creation_time=int(time.time() * 1000),
                rule_name=self.name,
                triggering_log=log,
                mitre_tactics=["TA0004"], # Privilege Escalation
                mitre_techniques=["T1068", "T1059"]
            )
            return alert, {"action": "ISOLATE_ENDPOINT", "host": log.get_field("host.hostname")}
            
        return None

