import json
import logging
import time
import threading
from abc import ABC, abstractmethod
from dataclasses import dataclass, field
from enum import Enum
from typing import Optional, Tuple, Dict, List, Any, Set

# Configure log output formatting
logging.basicConfig(level=logging.INFO, format='%(asctime)s [%(levelname)s] %(message)s')
logger = logging.getLogger("SmartSIEM.Engine")

class AlertSeverity(Enum):
    INFO = "INFO"
    LOW = "LOW"
    MEDIUM = "MEDIUM"
    HIGH = "HIGH"
    CRITICAL = "CRITICAL"

@dataclass
class SiemLog:
    """Represents a normalized security event log within the pipeline."""
    log_id: str
    tenant: str
    timestamp: int  # Epoch millisecond timestamp
    service: str
    event_type: str
    payload: Dict[str, Any]
    tags: Set[str] = field(default_factory=set)

    def get_field(self, path: str, default: Any = None) -> Any:
        """Extracts nested properties using dot-notation string paths."""
        keys = path.split('.')
        val = self.payload
        for key in keys:
            if isinstance(val, dict) and key in val:
                val = val[key]
            else:
                return default
        return val

@dataclass
class SiemAlert:
    """Represents an analytical security incident generated by a correlation rule matching."""
    alert_id: str
    title: str
    description: str
    severity: AlertSeverity
    creation_time: int
    rule_name: str
    triggering_log: SiemLog
    mitre_tactics: List[str] = field(default_factory=list)
    mitre_techniques: List[str] = field(default_factory=list)
    aggr_key: Optional[str] = None
    aggr_limit: Optional[int] = None

class SiemRule(ABC):
    """Abstract Base Class establishing structural interface standards for SIEM detection rules."""
    
    def __init__(self, name: str, description: str, service: str):
        self.name = name
        self.description = description
        self.service = service
        self.templates: Dict[str, str] = {"en": "Default alert template"}
        self.tenant_mapping: Dict[str, str] = {}

    @abstractmethod
    def evaluate(self, log: SiemLog) -> Optional[Tuple[SiemAlert, Optional[Dict[str, Any]]]]:
        """Processes a log entry to determine if alert thresholds are satisfied."""
        pass

    def get_localized_description(self, tenant: str, defaults: str) -> str:
        lang = self.tenant_mapping.get(tenant, "en")
        return self.templates.get(lang, defaults)

